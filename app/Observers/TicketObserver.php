    public function created(Ticket $ticket)
    {
        \Illuminate\Support\Facades\Mail::send(
            new \App\Mail\CreateTicketMail([
                'username' => $ticket->user->name,
                'email' => $ticket->user->email,
                'subject' => $ticket->subject,
                'content' => $ticket->content
